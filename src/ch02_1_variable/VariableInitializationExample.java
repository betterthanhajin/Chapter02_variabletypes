package ch02_1_variable;

public class VariableInitializationExample {

	public static void main(String[] args) {
		
		int value = 0;
		int result = value + 10;
		System.out.println(result);
		
		int a = 5;
		int r = 0;
		double area = r*r*3.141592;
		
		a = 10;
		a = 100;
		
		int c = 0;
		System.out.println(area + c + a);
		
	}

}
